    private void btnCheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckActionPerformed
        // TO read data for profile status
        try {
            st = dbconnection.createStatement();
            rs = st.executeQuery("SELECT * FROM user_registration" + " WHERE Username='" +txtUsername.getText()+"'");
            if (rs.next()) {
                btnSave.setEnabled(true);
                txtName.setText(rs.getString("Name"));
                txtMail.setText(rs.getString("Email"));
                txtDateofbirth.setText(rs.getString("DateofBirth"));
                txtBio.setText(rs.getString("Bio"));
                txtStatus.setText(rs.getString("Status"));
                JOptionPane.showMessageDialog(this, "Username is valid and cannot be changed", "info", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(this, "Username doesn't exist", "info", JOptionPane.INFORMATION_MESSAGE);
                btnSave.setEnabled(false);
                txtUsername.setText("");
            }
        }
        catch(SQLException e) {
            System.out.println("Failed to connect " + e.toString());
        }
    }//GEN-LAST:event_btnCheckActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TO delete data
        try {
            String sql = "DELETE FROM user_registration WHERE Username=?";
            ps = dbconnection.prepareStatement(sql);
            ps.setString(1, txtUsername.getText());
            
            if (ps.executeUpdate() > 0) {
            JOptionPane.showMessageDialog(this, "Account Deleted", "info", JOptionPane.INFORMATION_MESSAGE);
            ps.close();
            this.setVisible(false);
            new login().setVisible(true);
            }
        }
        catch (SQLException e) {
            System.out.println("Failed to Connect " + e.toString());
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

