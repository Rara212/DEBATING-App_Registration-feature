    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        try {
            ps = dbconnection.prepareStatement("UPDATE user_registration SET Name=?, Email=?, DateofBirth=?, Bio=?, Status=? WHERE Username=?");
            ps.setString(1, txtName.getText());
            ps.setString(2, txtMail.getText());
            ps.setString(3, txtDateofbirth.getText());
            ps.setString(4, txtBio.getText());
            ps.setString(5, txtStatus.getText());
            ps.setString(6, txtUsername.getText());
            
            if (ps.executeUpdate() > 0) {
                JOptionPane.showMessageDialog(this, "Profile has been changed", "info", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(this, "Failed to save new profile setting", "info", JOptionPane.INFORMATION_MESSAGE);
                ps.close();
            }
        }
        catch (SQLException e) {
            System.out.println("Failed to connect " + e.toString());
        }
    }//GEN-LAST:event_btnSaveActionPerformed

