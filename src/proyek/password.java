    private void btnCheckpassActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckpassActionPerformed
        // TODO add your handling code here:
        try {
            st = dbconnection.createStatement();
            rs = st.executeQuery("SELECT * FROM user_registration" + " WHERE Username='" +txtUsername.getText()+"'");
            if (rs.next()) {
                btnSavepass.setEnabled(true);
                JOptionPane.showMessageDialog(this,"Username Exist, please type your new password", "info", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(this, "Username doesn't exist", "info", JOptionPane.INFORMATION_MESSAGE);
                btnSavepass.setEnabled(false);
                txtUsername.setText("");
            }
        }
        catch(SQLException e) {
            System.out.println("Failed to connect " + e.toString());
        }
    }//GEN-LAST:event_btnCheckpassActionPerformed

