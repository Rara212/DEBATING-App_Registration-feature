    private void btnSavepassActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSavepassActionPerformed
        // TODO add your handling code here:
        try {
            ps = dbconnection.prepareStatement("UPDATE user_registration SET Password=? WHERE Username=?");
            ps.setString(1, txtNewpass.getText());
            ps.setString(2, txtUsername.getText());
            
            if (ps.executeUpdate() > 0) {
                JOptionPane.showMessageDialog(this, "Password has been changed", "info", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(this, "Failed to save new password", "info", JOptionPane.INFORMATION_MESSAGE);
                ps.close();
            }
        }
        catch (SQLException e) {
            System.out.println("Failed to connect " + e.toString());
        }
    }//GEN-LAST:event_btnSavepassActionPerformed

